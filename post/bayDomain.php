<h1 class="main-heading">Как выбрать доменное имя </h1>
<p class="descr">Что проверить перед покупкой</p>
<p class="descr">Уникальность. Чтобы найти свободное доменное имя, используйте сервис Whois.

<p class="descr">Whois (от английского who is — кто есть) — это общедоступная база данных о зарегистрированных доменных именах, IP-адресах и их владельцах. Её работу регулирует ICANN.</p>

<p class="descr">Доступ к базе можно получить на сайте любого регистратора доменов или на специализированном ресурсе. Например, на whois.com.</p>

<p class="descr">Если выбранный домен оказался занят, с помощью этой службы можно:</p>

<p class="descr">узнать возраст сайта;</p>
<p class="descr">узнать дату истечения срока регистрации домена;</p>
<p class="descr">связаться с владельцем домена;</p>
<p class="descr">найти информацию о регистраторе домена.</p>
<p class="descr">Всё это может пригодиться, если вы захотите перекупить домен.</p>

<p class="descr">История. Следует помнить, что регистрация домена — это аренда. Если доменное имя перестали оплачивать, то регистратор выставляет его на повторную продажу. У всех регистраторов есть магазин свободных имён. При этом возможно, что на таком домене раньше работал сайт: следовательно, домен имеет историю в памяти поисковиков. И если репутация у него плохая, то раскрутить сайт на этом домене будет сложно.</p>

<p class="descr">Для проверки истории интернет-ресурса нужно посетить глобальный веб-архив https://archive.org/. В нём хранится история каждого интернет-ресурса. Можно посмотреть, как выглядел сайт раньше, и оценить изменения.</p>
