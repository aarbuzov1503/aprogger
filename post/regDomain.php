<h1 class="main-heading">Как выбрать доменное имя </h1>
<p class="descr">Как выбрать доменное имя</p>
<p class="descr">Доменное имя может состоять из букв, цифр и дефисов. Его пишут строчными буквами, так как система DNS нечувствительна к регистру. Общие правила для доменных имён:</p>

<p class="descr">имя должно быть уникальным;</p>
<p class="descr">длина имени — от 2 до 63 символов. Если имя написано национальными буквами, то считается количество символов Punycode;</p>
<p class="descr">имя не может начинаться и оканчиваться дефисом и содержать два дефиса подряд;</p>
<p class="descr">в имени не должно быть пробелов и нижнего подчёркивания.</p>
<p class="descr">Для системы DNS вид домена не имеет значения, так как она воспринимает его как комбинацию букв и цифр. Но для пользователей имя важно, потому что они запоминают его.</p>

<p class="descr">Поэтому хорошее доменное имя:</p>

<p class="descr">Короткое. Так его легче написать в поисковой строке.</p>
<p class="descr">Запоминающееся. Если пользователь забудет адрес и начнёт искать его в поисковой системе, он может заинтересоваться ресурсами конкурентов.</p>
<p class="descr">Лёгкое для произношения. Пользователь должен без труда называть доменное имя в устной беседе.</p>
<p class="descr">Без сложных для транслитерации букв и сочетаний. Например, без щ, которая будет выглядеть как shch, и без ф с вариантами ph или f. <p class="descr">Также следует избегать визуально сложных для прочтения сочетаний: wowvova, lilit. Или трудных для понимания: avtorzhzhet.
Совпадает с тематикой сайта. Ключевое слово в имени хорошо с точки зрения SEO (search engine optimization) — оптимизации под поисковые системы.
Включает в себя название компании или бренда. Это повысит узнаваемость компании и может принести больше трафика — если пользователь будет искать компанию по названию, поисковая система с большой вероятностью покажет ему домен с ним. Если название слишком длинное, то можно использовать аббревиатуры или сокращения.</p>
<p class="descr">Не содержит цифр. Домены с цифрами запоминаются сложнее. Поэтому лучше их не использовать, если цифры не часть бренда.</p>
<p class="descr">Что нужно знать перед покупкой домена</p>
<p class="descr">Покупка домена — это устоявшееся выражение. На самом деле происходит регистрация домена — аренда выбранного имени на срок от одного года до десяти лет. Аренду можно продлить.</p>

<p class="descr">Длительность аренды и порядок продления зависят от выбранной доменной зоны. Например, имена в зонах .ru, .рф, .su можно арендовать максимум на год, а потом ежегодно продлевать. Нужную информацию по каждой доменной зоне можно найти на сайте регистратора доменных имён.</p>

<p class="descr">Если аренда домена закончилась, а плата не поступила, в большинстве случаев у владельца есть около месяца на восстановление имени. Когда выйдет и этот срок, домен освободится и вновь станет доступным для приобретения. Сроки и условия зависят от выбранной доменной зоны. Их нужно уточнять на сайте регистратора. </p>
